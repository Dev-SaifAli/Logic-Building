/*
1. Accepts a character.
2. Check whether character is a lowercase letter.
3. if (character is lowercase) -> Display "Entered character is a lowercase letter"
otherwise display "Entered character is not a lowercase letter"
*/

let ch = "s";
if(ch>="a" && ch<="z"){
    console.log("Entered character is a lowercase letter")
} else{
    console.log("Entered character is not a lowercase letter");
}
